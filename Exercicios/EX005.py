a = int(input("Input your first number: "))
b = int(input("Input your second number: "))

plus = a + b
minus = a - b
multi = a * b
divide = a / b
resto = a % b

print(f"{a} + {b} = {plus}")
print(f"{a} - {b} = {minus}")
print(f"{a} * {b} = {multi}")
print(f"{a} / {b} = {divide}")
print(f"{a} % {b} = {resto}")
